package me.mindex.autorest.web;

public class CountersInfo {

    private Long channelsCount;
    private Long scanedChannels;
    private Long wordsCount;
    private Long scanedWords;
    private Long videosCount;
	public Long getChannelsCount() {
		return channelsCount;
	}
	public void setChannelsCount(Long channelsCount) {
		this.channelsCount = channelsCount;
	}
	public Long getScanedChannels() {
		return scanedChannels;
	}
	public void setScanedChannels(Long scanedChannels) {
		this.scanedChannels = scanedChannels;
	}
	public Long getWordsCount() {
		return wordsCount;
	}
	public void setWordsCount(Long wordsCount) {
		this.wordsCount = wordsCount;
	}
	public Long getScanedWords() {
		return scanedWords;
	}
	public void setScanedWords(Long scanedWords) {
		this.scanedWords = scanedWords;
	}
	public Long getVideosCount() {
		return videosCount;
	}
	public void setVideosCount(Long videosCount) {
		this.videosCount = videosCount;
	}
}
